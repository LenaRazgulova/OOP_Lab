package cam.it_poket;

public class AnotheModel {
    public int film;
    public int mark;


    public AnotheModel(int film, int mark) {

        this.film = film;
        this.mark = mark;
    }

    public int getMark() {

        return mark;
    }

    public void setMark(int mark) {

        this.mark = mark;
    }

    public int getFilm() {

        return film;
    }

    public void setFilm(int film) {

        this.film = film;

    }
}
